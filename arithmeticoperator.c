#include <stdio.h>
int main (){
    // ARITHMETIC OPERATORS
    // + (addition)
    // - (substraction)
    // * (multiply)
    // / (divide)
    // % (modulus)
    // ++ (increment)
    // -- (decrement)
    int x = 2;
    int y = 4; 

    int z = x + y;
    int a = x - y;
    int b = x * y;
    int c = x / y;
    int d = x % y;
    printf("%d\n", z);
    printf("%d\n", a);
    printf("%d\n", b);
    printf("%d\n", c);
    printf("%d\n", d);
    return 0;
}